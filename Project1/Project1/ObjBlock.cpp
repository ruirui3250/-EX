//アクション
void CObjBlock::Action()
{
	//主人公の位置を取得
	CObjHero* hero = (CObjHero*)Objs::GetObj(OBJ_HERO);
	float hx = hero->GetX();
	float hy = hero->GetY();
	//m_mapの全要素にアクセス
	for (int i = 0; i < 10; i++)
	{
		for (int j = 0; j < 100; j++)
		{
			if (m_map[i][j] > 0)
			{
				//要素番号を座標に変換
				float x = j * 64.0f;
				float y = i * 64.0f;
				//主人公とブロックの当たり判定
				if ((hx + 64.0f > x) && (hy + 64.0f > y) && (hy < y + 64.0f))
				{
					//上下左右設定

					//vectorの作成
					float vx = hx - x;
					float vy = hy - y;

					//長さを求める。
					float len = sqrt((vx * vx + vy * vy));
					//角度を求める
					float r = atan2(vy, vx);
					r = r * 180.0f / 3.14f;
				}
			}
		}
	}

}

//ドロー
void CObjBlock::Draw()
{
	//描画カラー情報
	float c[4] = { 1.0f,1.0f,1.0f,1.0f };
	RECT_F src;//描画切り取り位置
	RECT_F dst;//描画先表示

	//切り取り位置の設定
	src.m_top = 0.0f;
	src.m_left = 0.0f;
	src.m_right = 64.0f;
	src.m_bottom = 64.0f;

	m_scroll -= 3.0f;//scroll実験用

	for (int i = 0; i < 10; i++)
	{
		for (int j = 0; j < 100; j++)
		{
			//表示位置の設定
			dst.m_top = i * 64.0f;
			dst.m_left = j * 64.0f+m_scroll;
			dst.m_right = dst.m_left + 64.0f;
			dst.m_bottom = dst.m_top + 64.0f;

			//描画
			Draw::Draw(9, &src, &dst, c, 0.0f);
		}
	}

}